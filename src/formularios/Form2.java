    private void sortearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sortearActionPerformed
        this.dado1.setVisible(false);
        this.dado2.setVisible(false);
        d1 = 0;
        d2 = 0;
        d1 = rnd.nextInt(6) + 1;
        dado1.setIcon(new ImageIcon(getClass().getResource("/img/dado" + d1 + ".png")));
        this.dado1.setVisible(true);
        d2 = rnd.nextInt(6) + 1;
        dado2.setIcon(new ImageIcon(getClass().getResource("/img/dado" + d2 + ".png")));
        this.dado2.setVisible(true);
        if (d1 > d2) {
            JOptionPane.showMessageDialog(null, "Comienza a atacar: " + nombre1);
            this.atacar111.setVisible(true);
            this.atacar222.setVisible(false);
            j1 = pais1;
            this.sortear.setEnabled(false);
        } else if (d2 > d1) {
            JOptionPane.showMessageDialog(null, "Comienza a atacar: " + nombre2);
            this.atacar111.setVisible(false);
            this.atacar222.setVisible(true);
            j2 = pais1;
            this.sortear.setEnabled(false);
        } else if (d1 == d2) {
            JOptionPane.showMessageDialog(null, "Empate, por favor vuelva a presionar botón SORTEAR");
        }

    }//GEN-LAST:event_sortearActionPerformed

