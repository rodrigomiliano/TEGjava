    private void turquiaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_turquiaActionPerformed
        pais = "turquia";
        pais1 = "turquia";
        pais2 = "turquia";
       
        if (jugadores == 2) {
            if (puntos1 == puntos2) {
                JOptionPane.showMessageDialog(null, nombre1 + "-" + "elige turquia");
                this.azulturquia.setVisible(true);
                puntos1++;
                this.jTextField3.setText(Integer.toString(puntos1));
            } else {
                JOptionPane.showMessageDialog(null, nombre2 + "-" + "elige turquia");
                this.rojoturquia.setVisible(true);
                puntos2++;
                this.jTextField4.setText(Integer.toString(puntos2));
            }
        }

        if (puntos2 == 9) {
            JOptionPane.showMessageDialog(null, "Presionar el boton SORTEAR para elegir quien empieza atacando");
            this.sortear.setEnabled(true);
            this.elegirpais.setEnabled(false);
        }

        this.turquia.setEnabled(false);
    }//GEN-LAST:event_turquiaActionPerformed

